package data.travail;

public class Heure {
	private int h ; 
	private int min ;
	

	public Heure(int h, int min) {
		this.h = h;
		this.min = min;
	}
	public int getH() {
		return h;
	}
	public void setH(int h) {
		this.h = h;
	}
	public int getMin() {
		return min;
	}
	public void setMin(int min) {
		this.min = min;
	}
	
}
